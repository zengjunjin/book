# ========== 实用函数 ==========

def get_prompt(prompt_key: str, variant: str = "user", **kwargs) -> str:
    """获取提示并填充变量"""
    try:
        template = PROMPTS.get(prompt_key, {}).get(variant, "")
        if kwargs:
            try:
                template = template.format(**kwargs)
            except:
                pass  # 格式化失败时返回原模板
        return template
    except:
        return ""


def list_prompts() -> list:
    """列出所有可用的提示"""
    return list(PROMPTS.keys())
